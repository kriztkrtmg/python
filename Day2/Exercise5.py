##Write a code to ask an input from the user which is a string and convert it to uppercase and lowercase.

my_input = input("Please input the string(Any word / Sentence) :")

uppercase = my_input.upper()
lowercase = my_input.lower()

print("Your input string is :", my_input)
print("Uppercase :", uppercase)
print("Lowercase :", lowercase)


##Output
##Please input the string(Any word / Sentence) :Hello World. GENESE cloud Academy
##Your input string is : Hello World. GENESE cloud Academy
##Uppercase : HELLO WORLD. GENESE CLOUD ACADEMY
##Lowercase : hello world. genese cloud academy
