'''Exercise 2 : To write a program that will convert Fahrenheit to Celsius in degrees.'''


fahrenheit = input("Enter the temperature in degree Fahrenheit :")

celsius = ((float(fahrenheit) - 32) * 5)/9

print("It's celsius degree value is :", celsius)

##output
##Enter the temperature in degree Fahrenheit :98 (Here, 98 is the value enter by user)
##It's celsius degree value is : 36.666666666666664
